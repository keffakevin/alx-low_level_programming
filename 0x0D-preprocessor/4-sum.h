#ifndef MAIN_H
#define MAIN_H
/**
 * SUM - Computes the sum of two numbers.
 * @x: The first number to sum.
 * @y: The second number to sum.
 *
 * Description: This macro computes the sum of two numbers x and y using the
 *              addition operator. It does not perform any error checking.
 *
 * Return: The sum of x and y.
 */
#define SUM(x, y) ((x) + (y))
#endif /* MAIN_H */
